public class People {

    private String name;

    private String id;



    public People(String name, String id) {

        super();

        this.name = name;

        this.id = id;

    }



    /**

     * @return name

     */

    public String getName() {

        return name;

    }



    /**

     * @param name 要设置的 name

     */

    public void setName(String name) {

        this.name = name;

    }



    /**

     * @return id

     */

    public String getId() {

        return id;

    }



    /**

     * @param id 要设置的 id

     */

    public void setId(String id) {

        this.id = id;

    }









}
